    public static int darker(int color, float factor) {
        int r = (int)((float)(color >> 16 & 255) * factor);
        int g = (int)((float)(color >> 8 & 255) * factor);
        int b = (int)((float)(color & 255) * factor);
        int a = color >> 24 & 255;
        return (r & 255) << 16 | (g & 255) << 8 | b & 255 | (a & 255) << 24;
    }
